public class FeedLecturerModel {

	private String lecturer_subject_id;
	private String user_id;
	private String subject_id;
	private String firstname;
	private String lastname;
	private String user_profile_photo;

	public String getLecturer_subject_id() {
		return lecturer_subject_id;
	}
	public void setLecturer_subject_id(String lecturer_subject_id) {
		this.lecturer_subject_id = lecturer_subject_id;
	}
	public String getUser_id() {
		return user_id;
	}
	public void setUser_id(String user_id) {
		this.user_id = user_id;
	}
	public String getSubject_id() {
		return subject_id;
	}
	public void setSubject_id(String subject_id) {
		this.subject_id = subject_id;
	}
	public String getFirstname() {
		return firstname;
	}
	public void setFirstname(String firstname) {
		this.firstname = firstname;
	}
	public String getLastname() {
		return lastname;
	}
	public void setLastname(String lastname) {
		this.lastname = lastname;
	}
	public String getUser_profile_photo() {
		return user_profile_photo;
	}
	public void setUser_profile_photo(String user_profile_photo) {
		this.user_profile_photo = user_profile_photo;
	}


}
