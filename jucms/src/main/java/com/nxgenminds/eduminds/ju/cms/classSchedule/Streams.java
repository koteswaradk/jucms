package com.nxgenminds.eduminds.ju.cms.classSchedule;

public class Streams
{
    private String created_date;

    private String stream_name;

    private String stream_duration;

    private String stream_id;

    private String stream_description;

    public String getCreated_date ()
    {
        return created_date;
    }

    public void setCreated_date (String created_date)
    {
        this.created_date = created_date;
    }

    public String getStream_name ()
    {
        return stream_name;
    }

    public void setStream_name (String stream_name)
    {
        this.stream_name = stream_name;
    }

    public String getStream_duration ()
    {
        return stream_duration;
    }

    public void setStream_duration (String stream_duration)
    {
        this.stream_duration = stream_duration;
    }

    public String getStream_id ()
    {
        return stream_id;
    }

    public void setStream_id (String stream_id)
    {
        this.stream_id = stream_id;
    }

    public String getStream_description ()
    {
        return stream_description;
    }

    public void setStream_description (String stream_description)
    {
        this.stream_description = stream_description;
    }
}
			
			