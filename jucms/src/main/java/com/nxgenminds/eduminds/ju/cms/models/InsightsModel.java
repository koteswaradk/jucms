package com.nxgenminds.eduminds.ju.cms.models;

public class InsightsModel {

	private String user_id;
	private String friends;
	private String profile_views;
	private String posts;
	private String likes;
	private String photos;
	private String videos;

	public String getUser_id() {
		return user_id;
	}
	public void setUser_id(String user_id) {
		this.user_id = user_id;
	}
	public String getFriends() {
		return friends;
	}
	public void setFriends(String friends) {
		this.friends = friends;
	}
	public String getProfile_views() {
		return profile_views;
	}
	public void setProfile_views(String profile_views) {
		this.profile_views = profile_views;
	}
	public String getPosts() {
		return posts;
	}
	public void setPosts(String posts) {
		this.posts = posts;
	}
	public String getLikes() {
		return likes;
	}
	public void setLikes(String likes) {
		this.likes = likes;
	}
	public String getPhotos() {
		return photos;
	}
	public void setPhotos(String photos) {
		this.photos = photos;
	}
	public String getVideos() {
		return videos;
	}
	public void setVideos(String videos) {
		this.videos = videos;
	}



}
